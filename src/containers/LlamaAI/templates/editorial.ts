/**
 * Editorial report template — renders a LlamaAI research-mode message as a
 * standalone HTML artifact. Pure function: no DOM, no fetching, no side effects.
 *
 * Always renders with DefiLlama branding in PR1; user-configurable brand kit
 * (logo, primary color, title/footer overrides) is a separate feature.
 */

import rehypeRaw from 'rehype-raw'
import rehypeSanitize from 'rehype-sanitize'
import rehypeStringify from 'rehype-stringify'
import remarkGfm from 'remark-gfm'
import remarkParse from 'remark-parse'
import remarkRehype from 'remark-rehype'
import { unified } from 'unified'
import { SANITIZE_SCHEMA } from '~/containers/LlamaAI/components/markdown/sanitizeConfig'
import { parseMessageToRenderModel } from '~/containers/LlamaAI/renderModel'
import type { Message } from '~/containers/LlamaAI/types'
import {
	escapeBareOrderedListMarkers,
	normalizeSourceUrl,
	processCitationMarkers
} from '~/containers/LlamaAI/utils/markdownHelpers'
import { escapeHtml } from '~/containers/LlamaAI/utils/textUtils'

export interface ChartImage {
	dataUrl: string
	title?: string
}

interface EditorialReportInput {
	message: Message
	/** Map of chart artifact id → exported chart (PNG + title). Generated by exportArtifact orchestrator. */
	chartImages: Map<string, ChartImage>
	/** Data URL for the DefiLlama wordmark used in "Powered by" line. Inlined for offline portability. */
	defillamaWordmarkDataUrl?: string
	/** Timestamp shown in header + footer. Defaults to now. */
	generatedAt?: Date
}

const REPORT_TITLE = 'DefiLlama Research Report'
// DefiLlama brand blue. Same value as the [#2172e5] literals across the SPA;
// duplicated here because the exported artifact has no access to app CSS tokens.
const PRIMARY_COLOR = '#2172E5'

// The export pipeline lets server-emitted markdown through with `allowDangerousHtml`
// (so processCitationMarkers's <citation-badge> tags survive), then runs the same
// rehype-sanitize schema that the in-chat ChatMarkdownRenderer uses. Without
// sanitization, an injected `<script>` in the source message would land in the
// exported artifact and execute in the recipient's browser.
const markdownProcessor = unified()
	.use(remarkParse)
	.use(remarkGfm)
	.use(remarkRehype, { allowDangerousHtml: true })
	.use(rehypeRaw)
	.use(rehypeSanitize, SANITIZE_SCHEMA)
	.use(rehypeStringify)

async function renderMarkdownToHtml(content: string, citations?: string[]): Promise<string> {
	const processed = escapeBareOrderedListMarkers(processCitationMarkers(content, citations))
	const file = await markdownProcessor.process(processed)
	return String(file)
}

function renderSourcesList(citations: string[]): string {
	const items = citations
		.map((rawUrl, idx) => {
			const url = normalizeSourceUrl(rawUrl)
			const safeUrl = escapeHtml(url)
			return `<li><a href="${safeUrl}" target="_blank" rel="noopener noreferrer"><span class="cite-num">${idx + 1}</span><span class="cite-url">${safeUrl}</span></a></li>`
		})
		.join('')
	return `<section class="sources"><h2>Sources</h2><ol class="sources-list">${items}</ol></section>`
}

function formatDate(date: Date): string {
	return date.toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })
}

function renderHeader(dateStr: string, wordmarkDataUrl: string | undefined): string {
	const headerLogoHtml = wordmarkDataUrl
		? `<img class="user-logo defillama-fallback" src="${escapeHtml(wordmarkDataUrl)}" alt="DefiLlama" />`
		: ''
	const poweredBy = wordmarkDataUrl
		? `<div class="powered-by"><span>Powered by</span><img src="${escapeHtml(wordmarkDataUrl)}" alt="DefiLlama" /></div>`
		: `<div class="powered-by"><span>Powered by DefiLlama</span></div>`
	return `<header class="report-header">
		<div class="header-row">
			<div class="header-left">${headerLogoHtml}</div>
			<div class="header-right">${poweredBy}<div class="report-date">${escapeHtml(dateStr)}</div></div>
		</div>
		<h1 class="report-title">${escapeHtml(REPORT_TITLE)}</h1>
	</header>`
}

function renderFooter(dateStr: string): string {
	return `<footer class="report-footer">
		<div class="footer-disclaimer">Data as of ${escapeHtml(dateStr)}. Source: DefiLlama and cited web sources. Web-sourced context is unverified by DefiLlama. NFA, DYOR.</div>
		<div class="defillama-attribution">Data via <a href="https://defillama.com" target="_blank" rel="noopener noreferrer">defillama.com</a></div>
	</footer>`
}

