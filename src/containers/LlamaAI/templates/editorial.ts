// Styles depend only on the fixed DefiLlama brand color in PR1, so build them
// once at module load instead of on every export.
const STYLES = buildStyles()

/**
 * Render a LlamaAI message (typically a research-mode response) as a complete
 * standalone HTML document. Returns the HTML as a string.
 */
export async function renderEditorialReport(input: EditorialReportInput): Promise<string> {
	const generatedAt = input.generatedAt ?? new Date()
	const dateStr = formatDate(generatedAt)

	const { blocks } = parseMessageToRenderModel(input.message)

	const bodyParts: string[] = []
	const sourcesParts: string[] = []
	for (const block of blocks) {
		if (block.type === 'markdown') {
			bodyParts.push(await renderMarkdownToHtml(block.content, block.citations))
		} else if (block.type === 'chart') {
			const chart = input.chartImages.get(block.artifactId)
			if (chart) {
				const altText = escapeHtml(chart.title ?? 'Chart')
				const caption = chart.title ? `<figcaption>${escapeHtml(chart.title)}</figcaption>` : ''
				bodyParts.push(
					`<figure class="chart"><img src="${chart.dataUrl}" alt="${altText}" loading="lazy" />${caption}</figure>`
				)
			}
		} else if (block.type === 'sources') {
			sourcesParts.push(renderSourcesList(block.citations))
		}
		// csv / md / alert / dashboard / image / action-group blocks are deliberately
		// excluded from the printed artifact. CSVs and markdown files are downloadable
		// adjacent artifacts; dashboards/alerts/actions are interactive surfaces with
		// no useful static equivalent. Future iterations may surface CSV/MD as a
		// "Related downloads" appendix.
	}

	const header = renderHeader(dateStr, input.defillamaWordmarkDataUrl)

	return `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8" />
<meta name="viewport" content="width=device-width, initial-scale=1" />
<title>${escapeHtml(REPORT_TITLE)}</title>
<style>${STYLES}</style>
</head>
<body>
<article class="report">
${header}
<div class="report-body">
${bodyParts.join('\n')}
</div>
${sourcesParts.join('\n')}
${renderFooter(dateStr)}
</article>
</body>
</html>`
}
